package dao.controllers;

import dto.controllers.CategoryDTO;
import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.util.ArrayList;
import java.util.List;
import utils.DBUtils;

/**
 *
 * @author Trong Duy
 */
public class CategoryDAO {
    private static final String ADD_CATE = "INSERT INTO Category(CategoryID, CategoryName, Status) VALUES(?, ? , 1) ";
    private static final String DUP = "SELECT CategoryName FROM Category where CategoryID = ?";
    private static final String SEARCH = "SELECT CategoryID, CategoryName, Status FROM Category";
    private static final String UPDATE = "UPDATE Category SET CategoryID = ?, CategoryName = ?, status = '1' where CategoryID = ?";
    private static final String DELETE = "UPDATE Category set status='0' where CategoryID=?";
    
    public boolean createCate(String categoryID, String categoryName) throws SQLException {
        boolean check = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(ADD_CATE);
                ptm.setString(1, categoryID);
                ptm.setString(2, categoryName);
                check = ptm.executeUpdate() > 0;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }

        }
        return check;
    }
    public boolean checkDupicate(String categoryID) throws SQLException {
         boolean check = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        ResultSet rs = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(DUP);
                ptm.setString(1, categoryID);
                rs = ptm.executeQuery();
                if (rs.next()) {
                    check = true;
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (rs != null) {
                rs.close();
            }
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return check;
    }
    public List<CategoryDTO> getAllCate() throws SQLException {
        List listCate = new ArrayList<>();
        Connection conn = null;
        PreparedStatement ptm = null;
        ResultSet rs = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(SEARCH);
                rs = ptm.executeQuery();
                while (rs.next()) {
                    String categoryID = rs.getString("CategoryID");
                    String categoryName = rs.getString("CategoryName");
                    int status = Integer.parseInt(rs.getString("Status"));
                    listCate.add(new CategoryDTO(categoryID, categoryName, status));
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (rs != null) {
                rs.close();
            }
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return listCate;
    }
    public boolean update(String categoryID, String categoryName) throws SQLException {
        boolean check = false;
        Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(UPDATE);
                ptm.setString(1, categoryID);
                ptm.setString(2, categoryName);
                ptm.setString(3, categoryID);
                check = ptm.executeUpdate() > 0;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }
        return check;
    }
    public boolean delete(String categoryID) throws SQLException {
        boolean check = true;
        Connection conn = null;
        PreparedStatement ptm = null;
        try {
            conn = DBUtils.getConnection();
            if (conn != null) {
                ptm = conn.prepareStatement(DELETE);
                ptm.setString(1, categoryID);
                check = ptm.executeUpdate() > 0 ? true : false;
            }
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (ptm != null) {
                ptm.close();
            }
            if (conn != null) {
                conn.close();
            }
        }

        return check;
    }
}
